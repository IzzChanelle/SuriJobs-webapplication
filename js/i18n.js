// ---- Helper ----
window.i18n = function(key) {
    const lang = window.LANG || 'nl';
    const dict = window.TRANSLATIONS[lang] || window.TRANSLATIONS.nl;
    return dict[key] !== undefined ? dict[key] : (window.TRANSLATIONS.nl[key] || key);
};

// Shorthand
window.__ = window.i18n;

// Update LANG when changed by topnav
window.addEventListener('storage', function(e) {
    if (e.key === 'sj_lang') {
        window.LANG = e.newValue || 'nl';
    }
});
